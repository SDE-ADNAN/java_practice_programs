public class Primitives {
    public static void main(String[] args) {
        //Primitive DataTypes
        //Primitive datatypes are the data types which could not be divided further in to more pieces 
        // e.g string can be divided into individual char  so it is a non 
        // primitive datatype but char is a primitive datatype

        //String is a non primitive datatype

        String name = "Adnan";
        System.out.println(name);

        //Some primitive datatypes 

        char character = 'A';
        System.out.println(character);

        int number = 64;
        System.out.println(number);

        float DecimalNumbers = 4.5f;
        System.out.println(DecimalNumbers);

        double LongDecimalNumbers = 45454.45454;
        System.out.println(LongDecimalNumbers);

        double LongIntegerNumbers = 343434343434343434l;
        System.out.println(LongIntegerNumbers);

        boolean check = true;
        System.out.println(check);




        
        
    }
}